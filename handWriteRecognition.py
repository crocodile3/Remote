from numpy import *

def img2vector(filename):
    """
    将图像转为向量
    :param filename:
    :return:
    """
    returnVect = zeros(1,1024)
    fr = open(filename)
    for i in range(32):
        lineStr = fr.readline()
        for j in range(32):
            returnVect[0,32*i+j] = int(lineStr[j])
    return  returnVect